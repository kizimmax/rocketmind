/** Короткий helper для создания Message. */
function m(
  idx: number,
  sessionId: string,
  role: Message["role"],
  content: string,
  createdAt: string,
  metadata?: MessageMetadata
): Message {
  return {
    id: `m_${sessionId}_${idx}`,
    conversation_id: sessionId,
    role,
    content,
    metadata,
    created_at: createdAt,
    is_read: true,
  };
}

