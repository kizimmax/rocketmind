#!/usr/bin/env node
/**
 * Sync Ivan's live OpenAPI спеку → docs/ivan-current-api.json + diff-отчёт.
 *
 * Иван деплоит SaaS-бэкенд на Amvera (r-accel-back-rocketmind), Swagger UI
 * лежит по адресу /api/docs/. Сама спека встроена в HTML через
 * swagger-ui-init.js — этот скрипт скачивает и парсит её,
 * сохраняет JSON-снимок и сравнивает с нашей `docs/saas-admin-api.yaml`.
 *
 * Usage:
 *   node scripts/sync-ivan-api.mjs
 *
 * После запуска:
 *   1. Снимок Ивана:   docs/ivan-current-api.json
 *   2. Diff-отчёт:     docs/api-sync-status.md
 *
 * Workflow:
 *   • Иван деплоит фичу → обновляет /api/docs автоматически (NestJS gen).
 *   • Мы запускаем этот скрипт → видим какие endpoints добавились/изменились.
 *   • Если у Ивана появился endpoint которого нет у нас — добавляем в наш
 *     yaml + помечаем как ✅ implemented.
 *   • Если у нас есть endpoint которого нет у него — это «ещё не сделано».
 */

import { writeFileSync, readFileSync } from "node:fs";
import { load } from "js-yaml";

const IVAN_URL = "https://r-accel-back-rocketmind.amvera.io/api/docs/swagger-ui-init.js";
const OUR_YAML = "docs/saas-admin-api.yaml";
const SNAPSHOT_JSON = "docs/ivan-current-api.json";
const REPORT_MD = "docs/api-sync-status.md";

async function fetchIvanSpec() {
  const res = await fetch(IVAN_URL);
  if (!res.ok) throw new Error(`Fetch ${IVAN_URL} → HTTP ${res.status}`);
  const js = await res.text();
  // Спека встроена в JS как `"swaggerDoc": { ... }`. Вырезаем JSON по balanced braces.
  const start = js.indexOf('"swaggerDoc":');
  if (start < 0) throw new Error("swaggerDoc not found in init.js");
  const openBrace = js.indexOf("{", start);
  let depth = 0;
  let end = -1;
  for (let i = openBrace; i < js.length; i++) {
    if (js[i] === "{") depth++;
    if (js[i] === "}") {
      depth--;
      if (depth === 0) {
        end = i + 1;
        break;
      }
    }
  }
  if (end < 0) throw new Error("swaggerDoc JSON balance failed");
  return JSON.parse(js.slice(openBrace, end));
}

function enumerateOps(spec) {
  const ops = [];
  for (const [path, methods] of Object.entries(spec.paths ?? {})) {
    for (const method of ["get", "post", "put", "patch", "delete"]) {
      if (methods[method]) {
        ops.push({
          key: `${method.toUpperCase()} ${path}`,
          method: method.toUpperCase(),
          path,
          summary: methods[method].summary ?? methods[method].operationId ?? "",
        });
      }
    }
  }
  return ops;
}

function normalisePathForCompare(p, basePrefix = "") {
  // Иван использует `/api/v1/...`, мы — `/api/...`. Уровняем для сравнения.
  return p.replace(/^\/api(\/v1)?/, "").replace(/^\/+/, "/").toLowerCase();
}

function indexOps(ops) {
  const m = new Map();
  for (const op of ops) {
    const key = `${op.method} ${normalisePathForCompare(op.path)}`;
    m.set(key, op);
  }
  return m;
}

async function main() {
  console.log(`→ Fetching Ivan spec: ${IVAN_URL}`);
  const ivanSpec = await fetchIvanSpec();
  writeFileSync(SNAPSHOT_JSON, JSON.stringify(ivanSpec, null, 2));
  console.log(`✓ Saved snapshot: ${SNAPSHOT_JSON}`);

  console.log(`→ Loading our spec: ${OUR_YAML}`);
  const ourSpec = load(readFileSync(OUR_YAML, "utf8"));

  const ivanOps = enumerateOps(ivanSpec);
  const ourOps = enumerateOps(ourSpec);

  const ivanIdx = indexOps(ivanOps);
  const ourIdx = indexOps(ourOps);

  const ivanKeys = new Set(ivanIdx.keys());
  const ourKeys = new Set(ourIdx.keys());

  const both = [...ivanKeys].filter((k) => ourKeys.has(k));
  const onlyIvan = [...ivanKeys].filter((k) => !ourKeys.has(k));
  const onlyOurs = [...ourKeys].filter((k) => !ivanKeys.has(k));

  const ivanSchemas = Object.keys(ivanSpec.components?.schemas ?? {});
  const ourSchemas = Object.keys(ourSpec.components?.schemas ?? {});

  const updatedAt = new Date().toISOString();

  let md = "";
  md += `# API sync status\n\n`;
  md += `_Auto-generated by \`scripts/sync-ivan-api.mjs\` at ${updatedAt}_\n\n`;
  md += `**Источник у Ивана:** ${ivanSpec.servers?.[0]?.url || "?"}\n\n`;
  md += `**Наш контракт:** \`${OUR_YAML}\`\n\n`;
  md += `## Summary\n\n`;
  md += `| Категория | Endpoints | Schemas |\n`;
  md += `|---|---|---|\n`;
  md += `| Уже у Ивана | **${ivanOps.length}** | **${ivanSchemas.length}** |\n`;
  md += `| В нашей спеке | **${ourOps.length}** | **${ourSchemas.length}** |\n`;
  md += `| Совпадают (по пути+методу) | **${both.length}** | — |\n`;
  md += `| Только у Ивана | **${onlyIvan.length}** | — |\n`;
  md += `| Только у нас (не имплементировано) | **${onlyOurs.length}** | — |\n\n`;

  md += `## ✅ Реализовано у Ивана + есть у нас\n\n`;
  md += "| Endpoint | Описание Ивана |\n|---|---|\n";
  both.sort().forEach((k) => {
    const op = ivanIdx.get(k);
    md += `| \`${op.method} ${op.path}\` | ${op.summary} |\n`;
  });
  md += `\n`;

  md += `## ⚙️ Только у Ивана (нет в нашей спеке — добавить)\n\n`;
  md += "| Endpoint | Описание |\n|---|---|\n";
  onlyIvan.sort().forEach((k) => {
    const op = ivanIdx.get(k);
    md += `| \`${op.method} ${op.path}\` | ${op.summary} |\n`;
  });
  md += `\n`;

  md += `## 📋 Только у нас (Иван ещё не реализовал)\n\n`;
  md += "| Endpoint | Наше описание |\n|---|---|\n";
  onlyOurs.sort().forEach((k) => {
    const op = ourIdx.get(k);
    md += `| \`${op.method} ${op.path}\` | ${op.summary} |\n`;
  });
  md += `\n`;

  md += `## Schemas — diff\n\n`;
  md += `### У Ивана (${ivanSchemas.length})\n\n`;
  ivanSchemas.forEach((s) => (md += `- \`${s}\`\n`));
  md += `\n### У нас (${ourSchemas.length})\n\n`;
  ourSchemas.forEach((s) => (md += `- \`${s}\`\n`));
  md += `\n`;

  writeFileSync(REPORT_MD, md);
  console.log(`✓ Saved report: ${REPORT_MD}`);
  console.log(`\nСводка:`);
  console.log(`  ✅ Match:       ${both.length}`);
  console.log(`  ⚙️ Только Иван: ${onlyIvan.length}`);
  console.log(`  📋 Только мы:   ${onlyOurs.length}`);
}

main().catch((err) => {
  console.error("ERR", err);
  process.exit(1);
});
